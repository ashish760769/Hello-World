#include<stdio.h>
void main()
{
    char c;
    short int b;
    int a;
    long int l;
    double d;
    float f;
    long long int ll;
    long double ld;

    printf("Size of char is %d\n",sizeof(c));
    printf("Size of short int is %d\n",sizeof(b));
    printf("Size of int is %d\n",sizeof(a));
    printf("Size of long int is %d\n",sizeof(l));
    printf("Size of double is %d\n",sizeof(d));
    printf("Size of float is %d\n",sizeof(f));
    printf("Size of long long int is %d\n",sizeof(ll));
    printf("Size of long double is %d\n",sizeof(ld));
}
